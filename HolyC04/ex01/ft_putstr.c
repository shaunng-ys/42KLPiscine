#include <unistd.h>

void	ft_putstr(char *str);

void	ft_putstr(char *str)
{
	int i = 0;
	while(str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(void)
{
	char str_1[] = "Print this out!";
	char *str_ptr = str_1;
	ft_putstr(str_ptr);
	return (0);
}
